var meshVertices = [];
var meshNormals = [];
var meshTexCoords = [];

window.onload = function init() {
    canvas = document.getElementById( "gl-canvas" );

    gl = WebGLUtils.setupWebGL( canvas );
    if ( !gl ) { alert( "WebGL isn't available" ); }

    gl.clearColor( 0.83, 0.76, 0.74, 1.0 );
	
	var program = initShaders( gl, "vertex-shader", "fragment-shader" );
	gl.useProgram( program );
	
	gl.enable(gl.DEPTH_TEST);
	
	prepareGeyik();
	
	var vBuffer = gl.createBuffer();
    gl.bindBuffer( gl.ARRAY_BUFFER, vBuffer);
    gl.bufferData( gl.ARRAY_BUFFER, new Float32Array(meshVertices), gl.STATIC_DRAW);
    
    var vPosition = gl.getAttribLocation( program, "vPosition");
    gl.vertexAttribPointer( vPosition, 3, gl.FLOAT, false, 0, 0);
    gl.enableVertexAttribArray( vPosition);

	var nBuffer = gl.createBuffer();
    gl.bindBuffer( gl.ARRAY_BUFFER, nBuffer);
	gl.bufferData( gl.ARRAY_BUFFER, new Float32Array(meshNormals), gl.STATIC_DRAW);
    
    var vNormal = gl.getAttribLocation( program, "vNormal");
    gl.vertexAttribPointer( vNormal, 3, gl.FLOAT, false, 0, 0);
	gl.enableVertexAttribArray( vNormal);
	
	var tBuffer = gl.createBuffer();
    gl.bindBuffer( gl.ARRAY_BUFFER, tBuffer);
    gl.bufferData( gl.ARRAY_BUFFER, new Float32Array(meshTexCoords), gl.STATIC_DRAW);
    
    var vTexCoords = gl.getAttribLocation( program, "vTexCoords");
    gl.vertexAttribPointer( vTexCoords, 2, gl.FLOAT, false, 0, 0);
	gl.enableVertexAttribArray( vTexCoords);
	

	var textSize = 256;
	var numRows = 16;
	var numCols = 16;

	var myTexels = new Uint8Array(4*textSize*textSize);

	for(var i= 0;i < textSize;++i){
		for(var j= 0;j < textSize;++j){
			var patchx = Math.floor(i/(textSize/numRows));
			var patchy = Math.floor(j/(textSize/numCols));
			var c = (patchx%2 !== patchy%2 ? 0 : 255);

			myTexels[4*i*textSize+4*j]=c;
			myTexels[4*i*textSize+4*j+1]=c;
			myTexels[4*i*textSize+4*j+2]=c;
			myTexels[4*i*textSize+4*j+3]=225;

		}
	}

	var texture = gl.createTexture();
	gl.bindTexture(gl.TEXTURE_2D,texture);

	gl.texImage2D(gl.TEXTURE_2D,0,gl.RGBA,textSize,textSize,0,gl.RGBA,gl.UNSIGNED_BYTE,myTexels);
	gl.generateMipmap(gl.TEXTURE_2D);

	gl.uniform1i(gl.getUniformLocation(program,"texmap"),0);
	

	modelViewMatrix = rotateX(0);
	projectionMatrix = ortho(-4.0, 4.0, -4.0, 4.0, -4.0, 4.0);
	
	modelViewMatrixLoc = gl.getUniformLocation( program, "modelViewMatrix" );
    projectionMatrixLoc = gl.getUniformLocation( program, "projectionMatrix" );

    gl.uniformMatrix4fv( modelViewMatrixLoc, false, flatten(modelViewMatrix));
	gl.uniformMatrix4fv( projectionMatrixLoc, false, flatten(projectionMatrix));
	
    document.getElementById("XButton").onclick = function(){
		modelViewMatrix = mult(rotateX(5), modelViewMatrix);
		gl.uniformMatrix4fv( modelViewMatrixLoc, false, flatten(modelViewMatrix));
		render();
	};
    document.getElementById("YButton").onclick = function(){
		modelViewMatrix = mult(rotateY(5), modelViewMatrix);
		gl.uniformMatrix4fv( modelViewMatrixLoc, false, flatten(modelViewMatrix));
		render();
	};
    document.getElementById("ZButton").onclick = function(){
		modelViewMatrix = mult(rotateZ(5), modelViewMatrix);
		gl.uniformMatrix4fv( modelViewMatrixLoc, false, flatten(modelViewMatrix));
		render();
	};
	
    document.getElementById("Wireframe").onclick = function(){
        for( var i=0; i<triangles.length; i+=3)
        gl.drawArrays( gl.LINE_LOOP, i,3 );
        Q=false;
        render();
	};
	
	document.getElementById("Shaded").onclick = function(){
        for( var i=0; i<triangles.length; i+=3)
        gl.drawArrays( gl.TRIANGLES,0, (meshVertices.length)/250);
        Q =true;
        render();
    };

	document.getElementById("Xlight").oninput = function() {
	        lightDirection = vec4(this.value,y,z,0.0);
	        gl.uniform4fv( gl.getUniformLocation(program,
	       "lightDirection"),flatten(lightDirection) );
	        x=this.value;
	        render(); 
	        };
	document.getElementById("Ylight").oninput = function() {
	        lightDirection = vec4(x,this.value,z,0.0);
	        gl.uniform4fv( gl.getUniformLocation(program,
	       "lightDirection"),flatten(lightDirection) );
	        y=this.value;
	        render(); 
	        };
	document.getElementById("Zlight").oninput = function() {
	        lightDirection = vec4(x,y,this.value,0.0);
	        gl.uniform4fv( gl.getUniformLocation(program,
	       "lightDirection"),flatten(lightDirection) );
	        z=this.value;
	        render(); 
	        };

  	ambientProduct = mult(ambientLight,ambientColor);
  	diffuseProduct = mult(lightlColor,diffuseColor);
  	specularProduct = mult(lightlColor,specularColor);

  
  	gl.uniform4fv(gl.getUniformLocation(program,"ambientProduct"), flatten(ambientProduct));
  	gl.uniform4fv(gl.getUniformLocation(program,"diffuseProduct"), flatten(diffuseProduct));
  	gl.uniform4fv(gl.getUniformLocation(program,"specularProduct"), flatten(specularProduct));
  	gl.uniform4fv(gl.getUniformLocation(program,"lightDirection"), flatten(lightlDirection));
  	gl.uniform1f(gl.getUniformLocation(program,"shininess"), materialShininess);

	render();
};

function prepareGeyik() {

	for (var i = 0; i < triangles.length; i+=9) {
		addTriangleVertexForIndices(triangles[i]-1,triangles[i+1]-1,triangles[i+2]-1);
		addTriangleVertexForIndices(triangles[i+3]-1,triangles[i+4]-1,triangles[i+5]-1);
		addTriangleVertexForIndices(triangles[i+6]-1,triangles[i+7]-1,triangles[i+8]-1);
		
	}
}
function changeLightPos()
{
    var posX = document.getElementById("Xlight").value;
    var posY = document.getElementById("Ylight").value;
    var posZ = document.getElementById("Zlight").value;
    gl.uniform4fv(gl.getUniformLocation(program,"lightDirection"), flatten( vec4(posX,posY,posZ,0.0)));
    render();
}
function addTriangleVertexForIndices(vIndex,tIndex,nIndex){
	meshVertices.push(vertices[3*vIndex],vertices[3*vIndex+1],vertices[3*vIndex+2]);

	meshTexCoords.push(textureCoords[2*tIndex],textureCoords[2*tIndex+1]);

	meshNormals.push(normals[3*nIndex],normals[3*nIndex+1],normals[3*nIndex+2])

}

function render() {
    gl.clear( gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);
    
    if(Q)
      gl.drawArrays( gl.TRIANGLES,0, (meshVertices.length)/3);
    else 
    {
       for( var i=0; i<triangles.length/3; i+=3)
       	gl.drawArrays( gl.LINE_LOOP, i,3 );
    }
}